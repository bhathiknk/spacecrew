             <h5>• Singularity –</h5>
              <p>The center point of the black hole. Here, the density is considered infinite. That
                is, scientists believe that all the laws of physics that we are used to are invalidated inside these
                black holes. And we cannot grasp this in any way. The reason is that not even a photon of light
                comes out.</p>
            
                <h5>• Event Horizon -</h5>
              <p> The limit at which objects pulled from the center of a black hole can return to
                the black hole. That is, the event horizon is the most basic factor that can be grasped to indicate
                that there is a black hole. There is no way for any object that passes this limit to escape the gravity
                of the black hole, at least not even light.</p>

                <h5>• Accretion disc -</h5>
                <p> The region outside the event horizon of the black hole is known by this name.
                  It consists of a spiral disk-like ring of matter revolving around the black hole, made up of massively
                  superheated gas and dust left over from objects being pulled into the black hole. The collisions
                  and motions of this dust and particles swirling around the black hole at high speeds create a
                  temperature of about one million Kelvin (the core of our sun is about 15 million Kelvin). This area
                  exists. Particles and dust from here are gradually drawn into the black hole after crossing the
                  event horizon. Also, because the temperature here is high, one way of transferring heat from this
                  accretion disc region is through radiation, which leaves the heat. What looks like a red disc around
                  the black hole is the radiation coming out of the accretion disc region. It can be seen in the images
                  taken of black holes.</p>

                  <h5>• Innermost Stable Circular Orbit (ISCO) -</h5>
                  <p> The dust and particles in the accretion disc are
                    not close to the event horizon and rotate in a stable orbit some distance away from it. This stable
                    orbit is called ISCO. The dust and particles that come further in from this orbit are pulled into the
                    black hole.</p>

                    <h5>• Photon sphere -</h5>
                      is the safe orbit for the dust and various particles in the accretion disc. If there are no light rays,
                      light photons have no mass, so they can rotate steadily around the black hole in an orbit at a
                      distance of 1.5 times the radius from the center of the black hole to the event horizon. The limit
                      within which these light photons rotate is called the photon sphere. Light photons traveling
                      through the table form a ring around the black hole. This allows some photons to escape the black
                      hole. Those photon clouds are what we see.</p>
                      <p>There may be some problem here because light photons have no mass. Why does light not come
                        back when it goes to a black hole? It feels like that if you think about it according to Newton's law
                        of gravity. But according to modern Einstein's theory of general relativity, gravity is caused by the
                        curvature of space and time. Because light travels in a straight line through time in normal space, </p>

                        <p>this bending is not well seen by us on Earth. Gravity and spacetime, on the other hand, bend much
                          more into black holes. We cannot see an incoming light photon again because the light photon
                          travels an infinite number of times. Also, the photons that exist outside of these black holes rotate
                          around the black hole in the form of rings. Imagine if you were there; you could even see your
                          backside.</p>

                   <h5>• Relativistic jets- </h5>
                  <p>A very small amount of matter or particles that cross the event horizon of
                    the black hole manage to escape without falling into the black hole. This matter comes out at a
                    very high speed, close to the speed of light. These outgoing matter streams are called relativistic
                    jets.</p>

